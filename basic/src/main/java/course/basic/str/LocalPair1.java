package course.basic.str;

/**
 * val1,val2
 *
 * @author cbooy
 * @date 2020-05-02
 */
public class LocalPair1 {

  private Object val1;

  private Object val2;

  public LocalPair1(Object val1, Object val2) {
    this.val1 = val1;
    this.val2 = val2;
  }

  public Object getVal1() {
    return val1;
  }

  public Object getVal2() {
    return val2;
  }
}
